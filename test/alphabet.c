#include <unistd.h>

int	main(void)
{
	char	a;

	a = 'a';
	while (a <= 'z')
	{
		write(1, &a, 1);
		if (a >= 65 && a <= 90)
		{
			a += 32;
			a++;
			write(1, &a, 1);
		}
		if (a >= 97 && a <= 122)
		{
			a -= 32;
		}
		a++;
		if (a == 'Z')
		{
			write(1, &a, 1);
			write(1, "\n", 1);
			return (0);
		}
	}
}
